    public boolean login(String numConta, String senha, boolean agencia) throws SQLException, RemoteException {
        boolean aux = false;
        String senhaBanco = "";
        try {
            if (agencia == true) {
                senhaBanco = servico.senha(numConta);
            }
            if (agencia == false) {
                senhaBanco = servico2.senha(numConta);
            }

            if (senhaBanco.equals(senha)) {
                aux = true;
            }
        } catch (RemoteException ex) {
            System.out.println("erro=>" + ex.getMessage());
        }
        if (aux == true) {
            return true;

        } else {
            return aux;
        }

    }

