    private void botaoLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botaoLoginActionPerformed
        try {
            if (agencia1.isSelected() == true) {
                agencia = true;
                try {
                    registry = LocateRegistry.getRegistry("172.16.1.108", 4000);
                    servico = (Servico) registry.lookup("TesteCurry1");
                    servico.connectionBanc();
                    System.out.println("Estou conectado, Servidor 1");
                    //servico = (Servico) Naming.lookup(localizacao);
                } catch (Exception ex) {
                    JOptionPane.showMessageDialog(null, "ERRO => " + ex.getMessage());
                }
                try {
                    String teste = num_contaText.getText();

                    if (!this.num_contaText.getText().trim().equals("")) {

                        if (this.login(this.num_contaText.getText(), this.senha.getText(), true) == true) {

                            this.dispose();
                            telaCliente.setClienteNome(servico.nome(this.num_contaText.getText()));
                            telaCliente.setSaldoText(servico.saldo(this.num_contaText.getText()));
                            telaCliente.setNumContaText(this.num_contaText.getText());
                            telaCliente.setVisible(true);
                        } else {
                            JOptionPane.showMessageDialog(null, "Senha incorreta, numero da correta incorreto ou agencia selecionada esta incorreta");
                        }
                    } else {
                        JOptionPane.showMessageDialog(null, "Algum campo esta nulo");
                        return;
                    }
                } catch (SQLException ex) {
                    Logger.getLogger(Cliente.class.getName()).log(Level.SEVERE, null, ex);
                } catch (RemoteException ex) {
                    Logger.getLogger(Cliente.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
            if (agencia2.isSelected() == true) {
                agencia = false;
                try {
                    registry = LocateRegistry.getRegistry("172.16.1.108", 4001);
                    servico2 = (Servico2) registry.lookup("TesteCurry2");
                    servico2.connectionBanc();
                    System.out.println("Estou conectado, Servidor 2");
                    //servico = (Servico) Naming.lookup(localizacao);
                } catch (Exception ex) {
                    JOptionPane.showMessageDialog(null, "ERRO => " + ex.getMessage());
                }
                try {
                    String teste = num_contaText.getText();
                    if (!this.num_contaText.getText().trim().equals("")) {
                        if (this.login(this.num_contaText.getText(), this.senha.getText(), false) == true) {
                            this.dispose();
                            telaCliente.setClienteNome(servico2.nome(this.num_contaText.getText()));
                            telaCliente.setSaldoText(servico2.saldo(this.num_contaText.getText()));
                            telaCliente.setNumContaText(this.num_contaText.getText());
                            telaCliente.setVisible(true);
                        } else {
                            JOptionPane.showMessageDialog(null, "Senha incorreta, numero da conta incorreto ou agencia selecionada esta incorreta");
                        }
                    } else {
                        JOptionPane.showMessageDialog(null, "Algum campo esta nulo");
                        return;
                    }
                } catch (SQLException ex) {
                    Logger.getLogger(Cliente.class.getName()).log(Level.SEVERE, null, ex);
                } catch (RemoteException ex) {
                    Logger.getLogger(Cliente.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
            if (!this.agencia1.isSelected() && !this.agencia2.isSelected()) {
                JOptionPane.showMessageDialog(null, "Determine qual agencia voce pertence");

            }
        } catch (Exception e) {

        }


    }//GEN-LAST:event_botaoLoginActionPerformed

